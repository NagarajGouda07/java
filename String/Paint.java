class Paint
{
   public static void wall(String brand)
   {
	 System.out.println("                                                                                   ");
     System.out.println("Brand is "+brand);
	 System.out.println("                                                                                   ");
   }
   public static void wall(String brand,String color)
   {
	 System.out.println("Method having two Parameters :");
     System.out.println("Brand is "+brand);
	 System.out.println("Color is "+color);
	 System.out.println("                                                                                   ");
   }
   public static void wall(String brand,double price)
   {
	 System.out.println("Method having two Parameters :");
     System.out.println("Brand is "+brand);
	 System.out.println("Price is "+price);
	 System.out.println("                                                                                   ");
   }
   public static void wall(String color,String type,double price)
   {
	 System.out.println("Method having three Parameters :");
     System.out.println("Color is "+color);
	 System.out.println("Type is "+type);
	 System.out.println("Price is "+price);
	 System.out.println("                                                                                   ");
   }
   

}